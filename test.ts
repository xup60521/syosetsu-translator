アウグスタが四歳の時、貴族院に入学する姉が礼儀作法の復習をしていると、その横でアウグスタも姉の真似を始めた。アウグスタが姉の真似をしたがるのは珍しいことではなかったが、この日はたまたま酔っていた父親が、祝福の真似をしたがるアウグスタに指輪を貸し与えたのだ。父親がアウグスタに指輪をはめると、指輪から小さな小さな祝福がふわりと舞い上がった。誰に教わらなくても、アウグスタは祝福を出すことができたのである。
両親は、魔力が少なく、かつ魔力の扱いが上手いアウグスタならば、家にある魔術具に魔力を込めることで生きのびることができると考えた。
アウグスタは早熟な子どもだった。
家の魔術具に魔力を込める方法を教わり、それが上手にできて褒められた時は単純に喜んだ。親に言われるままに腕を差し出し子供用魔術具を外された時も、なぜそのようなことをするのか疑問に思いつつも、されるがままだった。しかし、夜、床につき、落ち着いて考える時間ができれば、両親が自分を貴族になれない下働きとして育てるつもりだということに気づいた。気づいてしまった。
その精神的な衝撃に、幼いアウグスタの体は耐えられず、熱を出して寝込んだ。熱にうなされながらアウグスタは考えた。両親の願うまま下働きとして生きるのか、それ以外の道があるのか、ぐるぐると悩んだ。そして、熱が下がった時には決心していた。諦めないと。より良い未来を掴むために、あがくだけあがこうと。
アウグスタは両親の思惑に気づいていないふりをした。そのことに両親は気づかなかった。アウグスタが熱を出したのも様子がおかしかったのも、子供用魔術具を外したせいで魔力のバランスが崩れたからだと思ったのだ。
第一の関門は七歳で行う洗礼式とお披露目だった。両親はアウグスタの洗礼式をするつもりがないのだろう。そう考えたアウグスタは、どうすれば洗礼を受け、冬のお披露目に参加できるか頭をひねった。
アウグスタは、姉が貴族院に向けてフェシュピールの復習をするのを、横で熱心に聞いた。指使いを目で追い、記憶しながら。そして、姉の練習が終わると、その二学年の課題曲を弾こうとした。
それを見た姉は、苦笑しながらも優しく教えてくれた。両親は、アウグスタがまた姉の真似をしていると、温かい目で見ていた。
アウグスタの家では、一つのフェシュピールを姉妹で共有している。貴族院で使えるような大きさのフェシュピールであるため、五歳のアウグスタには少し大きい。しかし、そのフェシュピールを使って、アウグスタは来る日も来る日も練習した。
秋になり、アウグスタは六歳になった。しかし、フェシュピールの曲はまだ弾けるようになっていなかった。それでも練習を続けた。
冬になり姉が貴族院にフェシュピールを持って行ってしまってからも、その二学年の課題曲を歌いながら指を動かしていた。母親に「随分と気に入ったのね」と笑われるくらい繰り返した。
その甲斐あってか、季節が巡って春が終わるころには、その曲を上手に弾きこなすことができるようになった。
両親の前で演奏すると、アウグスタの頑張りを見てきた両親は「すごい」と褒めた。アウグスタは喜び、満面の笑みを浮かべて言った。
「わたくし、七歳のお披露目でこの曲を演奏します」
両親は「あなたのお披露目はしませんよ」と言うことができずに黙り込んだ。彼らは二人きりになった時に相談した。
アウグスタの頑張りを無下にするのは心苦しい。フェシュピールが上達したため、お披露目で披露すれば目を引くだろう。あまり美しくもなく魔力も少ない娘だが、フェシュピールの腕前により、条件の良い愛妾の道が開けるかもしれない。
両親はアウグスタのお披露目をすることにした。
父親は子供用魔術具に似た形の腕輪と洗礼式で与える指輪を用意した。母親は派閥の貴族達から情報を集め、アウグスタと共にお披露目をする中級貴族の子が何を演奏するのか確認した。
両親はアウグスタに腕輪をはめると言った。
「あの曲をお披露目で弾いてはいけません」
中級貴族の子より難しい曲を弾けば、悪目立ちして反感を買い、自分たちの身を危うくするからだ。両親は、もっと簡単な曲を提示した。
「この曲を弾きなさい。簡単な曲ですが、その分、誰よりも上手に弾くのですよ」
「はい。お父様、お母様」
アウグスタは心から喜んだ。同時に、下級貴族としての生き方も学んだ。
その年の秋、両親は親戚を集めてアウグスタの洗礼式をした。そして、アウグスタは冬のお披露目に参加することができたのである。
第二の関門は貴族院だった。アウグスタは両親からの愛情を感じていた。しかし同時に、両親が弟のために自身の子供用魔術具を外すのではなく、アウグスタの魔術具を外したという事実にも気づいていた。愛情は絶対的なものでもなければ、無限に湧き出てくるものでもないのだ。
与えることができる余裕のある分しか、愛情は与えられない。そして、貴族院に入学し卒業するにはとても金がかかる。アウグスタは、自分の家にそれだけの金銭的な余裕がないことに気づいていた。
アウグスタは困った。困っているのはその件だけではなかった。成長したせいか、家の魔術具に注いでも魔力が余るようになってきたのである。裕福でないアウグスタの家庭で使う魔術具は、そう多くないのだ。
とりあえずアウグスタは、姉が語ってくれる貴族院の話で出てきた魔力圧縮を参考にして、魔力を抑え込んだ。しかし、それが一時的な措置でしかないことはアウグスタにも分かっていた。
（必要なのは魔石よね。魔力を込める魔石。そもそも貴族院の授業で用いる魔石を貴族院に入学する十歳の冬までに染め終えなければいけないのだから、その魔石を用意してもらえさえすれば解決する話なのだわ）
アウグスタは両親と話し合う決断をした。
「貴族院入学の準備を始めようと思います。魔石をください」
両親は、「とうとうこの日が来てしまったか」というような沈痛な様子で「それはできない」とアウグスタに答えた。弟の腕から子供用魔術具を外すことはできない、と。
「それは当然です。私も幼い弟から子供用魔術具を取り上げるつもりはありません。私はそれが無くても何も困っていません。わたくしが欲しいのは魔石です。魔石をもらえれば自分で染め上げます」
両親は顔を見合わせた。確かに、アウグスタは一年半もの間、家の魔術具に魔力を込めているのだ。きっと、自分で魔石を染め上げることだってできるだろう。魔力さえ豊富ならば。
「残念だがアウグスタ、お前の魔力量では入学までの間に、必要な魔石を全部染めることは難しいだろう。仮に染められたとしても、貴族院での授業について行くのは厳しいと思う。無理をして貴族院に入ることはないのだ」
やっぱり、とアウグスタは思った。
（やっぱり、お父様とお母様は私を貴族院に入れるつもりがなかったのね）
洗礼式はしたけれど、貴族としてのアウグスタは、貴族院に入学する前にいなくなる予定だったのだろう。何らかの表向きの理由をつけて。
予想していたこととはいえアウグスタは悲しくなった。しかし、それを表に出さずにアウグスタは両親の説得にかかった。
「え？
そんなことはないと思います。今だって、家の魔術具に魔力を込めても魔力が余ってしまっていますもの。……では、お父様、こうしましょう。試しに魔石をわたくしに下さい。九歳になるまでに貴族院で必要になる数の魔石に魔力を込められなければ、わたくしは貴族院に行きません。その代わり、魔力を込められた時は、十歳で貴族院に行かせて下さい」
それで本人が納得できるのならばと、両親はその提案を飲んだ。
一度に全ての魔石を購入することはできなかったため、父親はひとまず、三個の魔石を手に入れてアウグスタに渡した。
アウグスタは一つ魔石を染めると、中三日休んでから次の魔石を染めた。次の魔石も、中三日休んでから染めた。そのため、三個染めるのに九日かかった。
アウグスタが両親に魔石を見せに行くと、両親とその場にいた姉が、驚きに固まった。そのあまりの様子に、アウグスタはたじろいた。
「成長して魔力量が増したのか。だが、それにしても……」
父親の呟きにより、自分が魔石を早く染めすぎたことに気づいたアウグスタは、あわてて言い訳をした。
「たぶん、子供用魔術具で少しずつ魔力を込めるより、一気に染め上げる方が効率よく染まるのでしょう」
「そうか。そうだな。追加の魔石はまだ手に入れていないのだ。準備するのでもう少し待て」
父親はそう言って話を切り上げた。
その夜、両親は話し合った。
「アウグスタの魔力量が予想以上に増えている。貴族院に行かせても良いのではないか」
「けれど、三人とも貴族院に行かせるだけのお金はうちにはありません」
「ちょうど上の子が卒業してからの入学だ。道具などは、そのまま使いまわせるし、学費だけならなんとかなるかもしれない」
「そうですね。貴族院用の服も……小さくて着られなくなった服をばらして布地などを再利用するつもりでしたが、アウグスタ用に取っておけば……。いいえ、やはり難しいでしょう。上の子の嫁入りの準備がありますし、下の子の入学の年はアウグスタの卒業の年に重なります」
「……そうだな。だが、可能性がないわけではない。上の子の嫁ぎ先やアウグスタの縁談によっては援助を得られるかもしれない。そうなったときに入学できるよう、できる準備だけでも進めておこう」
「そうですね。今はそうしましょう」
両親もできるならばアウグスタを貴族院に行かせてやりたかったのだ。二人は決断を先送りにして家計をやりくりし、アウグスタのための魔石を入手した。
必要な数の魔石を手に入れて安心したアウグスタは、今度はもう少しゆっくりとしたペースで魔石を染めることにした。それでも九歳までに染め終えることができるし、魔力が溢れそうになった時のために、まだ染めていない魔石を残しておきたいと思ったからだ。それに、姉を刺激したくなかったということもある。
それまで妹を可愛がり、いろいろなお話をしてくれていた姉とは、距離ができ始めていた。
アウグスタの家は最大派閥ヴェローニカ派に組み込まれていたが、ヴェローニカの更迭以降、父親は中立派に近づきたいと考えていた。その方針で姉の嫁ぎ先を探していたのだが、同じように考える者も多いらしく、なかなか良い縁談がない。まだ嫁ぎ先は決まっていなかった。
父親が妹の入学のために労力を割けば、その分、姉の縁談を探す時間が無くなる。また、妹の貴族院入学の費用を工面することになれば、その分だけ姉の嫁入り準備が怪しくなる。そのことに姉が気づいたのである。妹の魔力量が増え、妹の価値が上がるほど、自分の立場が厳しくなるのだ、と。
アウグスタは大好きな姉に嫌われたくはなかった。それなので、魔力を染めるペースを落として、自分の魔力量をごまかそうとした。魔力の圧縮で乗り越えたり、こっそり魔力を消費する方法を考えたりした。
まず、無理やり魔力を外に出そうとしたが、威圧のようになるだけで、魔力自体はあまり減らなかった。体への負担も大きい。一方で、祝福は魔力の減る感覚があったので、アウグスタは観葉植物を隠し部屋に持ち込み、それに対して祝福を送ったり祈りを捧げたりした。
姉の話に出てきた身体強化ができるようになると、さらに魔力を消費できるようになった。ただ、姉が苦労した身体強化を自分が修得したと知られるわけにはいかなかった。そのため、力加減を間違えそうな手足にかけることはせず、もっぱら目や耳や鼻を強化した。下働きのおしゃべりを聞いたり、夕飯のメニューを当てたりして遊んだ。
ある日、耳を身体強化したアウグスタは、子どもに隠れて相談する両親の話を聞いてしまった。金銭的な問題により、自分が貴族院に入学できるかどうか、まだ分からないことを知ってしまった。
アウグスタは何とかしなければと、自分のできることを探した。そして冬の子供部屋で領主候補生のローゼマインがお話を集めていたと伝え聞いたことを思い出した。
（昨年は治療のため子供部屋にいらっしゃらなかったが、たくさんのお話を集めておけば、今年の冬にローゼマイン様とお会いした時、お近づきになれるかもしれない。ローゼマイン様からの覚えがめでたくなり、ローゼマイン様と仲の良い子と親しくなれれば、お父様の役に立てるかもしれない）
アウグスタはお話を集め始めた。母親に聞くだけでなく、下働きの平民にも聞きに行った。そして、聞いたお話を上手に話せるよう練習した。
しかし、次の冬も、ローゼマインは子供部屋に来なかった。その次の冬、ローゼマイン様は回復して表舞台に出てきたようだが、すぐに貴族院へ行ってしまった。お話を買い取ってもらった子もいたようだが、お話を木札に書き留めていなかったアウグスタはその対象にならなかった。
九歳のアウグスタは、耳を澄まして情報を集めたり、人間関係の把握に努めたりしながらも、じりじりと焦っていた。いつ、両親が自分の貴族院入学を諦めてしまうか分からない。
それもあり、春を寿ぐ宴でヴィルフリートとローゼマインの婚約を知ったアウグスタは、賭けに出ることに決めた。
「お父様、ヴィルフリート様の筆頭側仕えであるオズヴァルト様の側仕えに伝手はございませんか。オズヴァルト様に、わたくしをヴィルフリート様の側近として推薦してくださいませ」
仰天する父親にアウグスタは、ヴィルフリートにとって自分を側近にする利を説明した。
今のヴィルフリート様の側近に女性はいない。婚約者となったローゼマイン様との関係を円滑にするためにも女性の側近がいた方が良い。それは共に貴族院に行ける年代が望ましい。ただ、めぼしい貴族はシャルロッテ様の側近になっているし、婚約したこの時期に恋愛対象にもなりうる年頃の女性を側近にすれば眉を顰める者もいるだろう。ただでさえヴィルフリート様はローゼマイン様に気を遣わなければならない立場だ。しかし、下級貴族で容姿も十人並みの自分ならば、その批判を最小限に抑えられる。下級貴族を側近に迎えることについては、ローゼマイン様がフィリーネを側近にしたばかりなので問題ない。自分は、ヴィルフリート様とローゼマイン様が良好な関係を維持するための潤滑油になってみせる。
「具体的な事例も提示する必要があるでしょう。お父様、木札とインクを用意してくださいませ。そこにローゼマイン様が収集しているお話をわたくしが書き出します。それを手土産の一つとして持って行きこう言ってくださいませ。『まずは婚約の記念に贈り物をしてはいかがでしょうか。ローゼマイン様が収集していると聞いて、娘がお話を集めておりました。これをお贈りすれば、ローゼマイン様はお喜びになるでしょう。ご希望ならば、紙への清書を娘にさせます。そういうこまごまとした雑用をさせる者が一人いれば、便利かと存じます』と」
（ああ、そうか。この夏に行う姉の星結びの準備で家計が苦しいのを、アウグスタも分かっているのだな。自分が貴族院に行けるかどうかも危ういと。だからこそ、このような無茶を言うのだろう。袖の下もたいした額を用意できないので無理だろうが、何もしないまま貴族院を諦めさせるのは忍びない。言う通りにしてやろう）
「分かった。しかし、オズヴァルト様まで話を通すためには、それだけの金がかかる。この件を進めて上手くいかなければアウグスタが貴族院に入学するのは難しくなるが、それでもやるか？」
「はい。その場合はわたくしが病に臥せったことにして、貴族院入学を先送りにしてください」
一年遅れの瑕疵をつけてアウグスタを貴族院に送ったとしても、それに見合うだけの嫁入り先は見つからないだろう。そのままずるずると貴族院に入学できずに終わる。それをアウグスタも分かっていたが、覚悟を決めてそう答えた。
オズヴァルトの側仕えは、身の程知らずな父娘の提案を主に伝えた。もらった袖の下の分の働きとして、一言伝えただけだった。だから、オズヴァルトがその娘に会うと言った時には驚いた。
オズヴァルトは、ヴィルフリートに女性の側近がいないことについて不便を感じていたところだった。貴族院で女性のお茶会に参加することになった時にローゼマインの側近を借りるしかなかった。ローゼマインの流行は女性向けのものが多い。今後のことを考えても、女性の側近はいた方が便利だ。
（それに、閨教育の件もある）
今後、ヴィルフリートが成長した時に、その教材となる女性を用意することは難しいだろう。神殿がローゼマインに掌握されている以上、花捧げの巫女を手配することはできない。また、貴族の女性から見繕うにしても、政敵に情報を流すような女では困る。第二夫人や第三夫人になりたがる相手でも困る。口が堅くて、ヴィルフリートの害にならないような女を探さなければならない。
情勢を考えれば、正直なところ難しい。しかし、ライゼガングの娘であるローゼマインに隙を見せるわけにはいかない。具体的な教育をしないで閨へ送り出すという選択肢はない。
（最悪の場合は、平民の女をさらってきて教材にした後、口封じのため処分するしかない。しかし、それだと魔力の扱いについては試せない。また、ヴィルフリート様が平民を相手にしたなどと人の口に上らないよう、よりいっそう秘密裏にことを進めなければならない。できれば避けたい）
「便利に使える下級貴族の娘を側近として取り込むのも悪くない、か」
アウグスタと面談したオズヴァルトは、いくつかの問答の後にこう述べた。
